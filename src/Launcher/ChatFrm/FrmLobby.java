    // Método para refrescar la lista
    private void cargarListaSalas() {
        javax.swing.DefaultListModel<InfoSala> modeloLista = new javax.swing.DefaultListModel<>();
        java.util.Map<String, String> misSalas = salasManager.obtenerSalas();
        
        for (String nombreSala : misSalas.keySet()) {
            String ipYPuerto = misSalas.get(nombreSala);
            String[] partes = ipYPuerto.split(":");
            
            String ip = partes[0];
            // Si tiene 3 partes (IP:TCP:PUERTO), agarramos el protocolo. Si es viejo (IP:PUERTO), asumimos TCP.
            String protocolo = partes.length == 3 ? partes[1] : "TCP"; 
            int puerto = Integer.parseInt(partes[partes.length - 1]);
            
            InfoSala info = new InfoSala(nombreSala, ip, puerto, protocolo);
            checarEstadoPing(info); 
            
            modeloLista.addElement(info);
        }
        
        listaSalas.setModel(modeloLista); 
        listaSalas.setCellRenderer(new RenderizadorSala());
    }

