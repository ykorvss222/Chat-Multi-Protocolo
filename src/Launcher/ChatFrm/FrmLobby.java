    private void btnCambiarFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarFotoActionPerformed
        // TODO add your handling code here:  
        java.io.File carpeta = new java.io.File("avatares");
        
        // filtro de imágenes
        java.io.File[] archivos = carpeta.listFiles((dir, name) -> 
            name.toLowerCase().endsWith(".png") || name.toLowerCase().endsWith(".jpg")
        );

        if (archivos == null || archivos.length == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "No se encontraron imágenes. Asegúrate de tener una carpeta llamada 'avatares' con imágenes en la raíz del proyecto.", 
                "Error de Avatares", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // configuracion de lista
        javax.swing.DefaultListModel<javax.swing.JLabel> modeloLista = new javax.swing.DefaultListModel<>();
        // Carga de imágenes
        for (java.io.File f : archivos) {
            javax.swing.ImageIcon iconoOriginal = new javax.swing.ImageIcon(f.getAbsolutePath());
            javax.swing.ImageIcon iconoChico = GestorImagenes.redimensionarIcono(iconoOriginal, 40, 40);
            
            javax.swing.JLabel etiqueta = new javax.swing.JLabel("  " + f.getName().replace(".png", ""), iconoChico, javax.swing.JLabel.LEFT);
            
            etiqueta.putClientProperty("archivoRuta", f); 
            
            modeloLista.addElement(etiqueta);
        }

        javax.swing.JList<javax.swing.JLabel> listaAvatares = new javax.swing.JList<>(modeloLista);
        listaAvatares.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
        
        listaAvatares.setCellRenderer(new javax.swing.ListCellRenderer<javax.swing.JLabel>() {
            public java.awt.Component getListCellRendererComponent(javax.swing.JList<? extends javax.swing.JLabel> list, javax.swing.JLabel value, int index, boolean isSelected, boolean cellHasFocus) {
                if (isSelected) {
                    value.setBackground(list.getSelectionBackground());
                    value.setForeground(list.getSelectionForeground());
                    value.setOpaque(true);
                } else {
                    value.setBackground(list.getBackground());
                    value.setForeground(list.getForeground());
                    value.setOpaque(false);
                }
                return value;
            }
        });

        // lista en un panel
        javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(listaAvatares);
        scrollPane.setPreferredSize(new java.awt.Dimension(250, 300)); 

        int seleccion = javax.swing.JOptionPane.showConfirmDialog(
                this, 
                scrollPane, 
                "Elige tu Avatar",
                javax.swing.JOptionPane.OK_CANCEL_OPTION, 
                javax.swing.JOptionPane.PLAIN_MESSAGE
        );

        // carga de iamgen
        if (seleccion == javax.swing.JOptionPane.OK_OPTION && listaAvatares.getSelectedValue() != null) {
            javax.swing.JLabel seleccionado = listaAvatares.getSelectedValue();
            
            java.io.File archivoAvatar = (java.io.File) seleccionado.getClientProperty("archivoRuta");
            
            // Convertimos a Base64 y guardamos
            String nuevoBase64 = GestorImagenes.codificarImagenABase64(archivoAvatar);
            perfil.guardarAvatar(nuevoBase64);
            
            javax.swing.ImageIcon iconoFinal = GestorImagenes.decodificarBase64AImagen(nuevoBase64);
            lblAvatar.setIcon(GestorImagenes.redimensionarIcono(iconoFinal, 100, 100));
        }
    }//GEN-LAST:event_btnCambiarFotoActionPerformed

    private void btnAgregarSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarSalaActionPerformed
        // TODO add your handling code here:
        String nombre = javax.swing.JOptionPane.showInputDialog(this, "Nombre de la sala (Máx 20 caracteres):");
        if (nombre == null || nombre.trim().isEmpty()) return;
        
        // --- VALIDACIÓN ---
        if (nombre.length() > 20) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "El nombre es demasiado largo. Usa 20 caracteres máximo.", 
                "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String ip = javax.swing.JOptionPane.showInputDialog(this, "IP del Servidor:");
        if (ip == null || ip.trim().isEmpty()) return;
        
        String[] opciones = {"TCP", "UDP"};
        String protocoloElegido = (String) javax.swing.JOptionPane.showInputDialog(
                this, "Selecciona el Protocolo:", "Tipo de Red", 
                javax.swing.JOptionPane.QUESTION_MESSAGE, null, opciones, opciones[0]);
                
        if (protocoloElegido == null) return; // Si cancela
        
        // Guardamos la IP y el protocolo
        salasManager.agregarSala(nombre, ip + ":" + protocoloElegido, 7777);
        cargarListaSalas();
    }//GEN-LAST:event_btnAgregarSalaActionPerformed

    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        // TODO add your handling code here:
        InfoSala salaSeleccionada = listaSalas.getSelectedValue();
        
        if (salaSeleccionada != null) {
            // Extraemos los datos 
            String ipServidor = salaSeleccionada.ip;
            int puertoServidor = salaSeleccionada.puerto;
            String protocolo = salaSeleccionada.protocolo;
            
            // Verificamos si la sala contestó
            if (!salaSeleccionada.online) {
                int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, 
                    "El servidor parece estar apagado. ¿Quieres intentar conectarte de todos modos?", 
                    "Servidor Offline", 
                    javax.swing.JOptionPane.YES_NO_OPTION, 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
                    
                if (respuesta != javax.swing.JOptionPane.YES_OPTION) {
                    return; // Si dice que no, cancelamos la conexión
                }
            }
            
            System.out.println("Conectando a IP: " + ipServidor + ":" + puertoServidor + " por " + protocolo + " con usuario: " + perfil.getUsername());
            
            String claveSala = ipServidor + ":" + puertoServidor;

            if (chatsAbiertos.containsKey(claveSala)) {
                javax.swing.JFrame chatGuardado = chatsAbiertos.get(claveSala);
                
                if (chatGuardado.isDisplayable()) {
                    chatGuardado.setVisible(true);
                } else {
                    FrmChat chat = new FrmChat(salaSeleccionada, perfil);
                    chatsAbiertos.put(claveSala, chat);
                    chat.setVisible(true);
                }
            } else {
                FrmChat chat = new FrmChat(salaSeleccionada, perfil);
                chatsAbiertos.put(claveSala, chat);
                chat.setVisible(true);
            }
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona una sala de la lista.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnConectarActionPerformed

    private void listaSalasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaSalasMouseClicked
        // TODO add your handling code here:
      
        // Si es click derecho mostrar opciones
        if (javax.swing.SwingUtilities.isRightMouseButton(evt)) {
            
            int row = listaSalas.locationToIndex(evt.getPoint());
            listaSalas.setSelectedIndex(row); 
            
            InfoSala salaSeleccionada = listaSalas.getSelectedValue();
            if (salaSeleccionada == null) return;

            javax.swing.JPopupMenu menu = new javax.swing.JPopupMenu();
            javax.swing.JMenuItem itemEliminar = new javax.swing.JMenuItem("🗑️ Eliminar '" + salaSeleccionada.nombre + "'");
            
            itemEliminar.addActionListener(e -> {
                int confirmar = javax.swing.JOptionPane.showConfirmDialog(this, 
                    "¿Estás seguro de eliminar esta sala?", "Eliminar", 
                    javax.swing.JOptionPane.YES_NO_OPTION);
                if (confirmar == javax.swing.JOptionPane.YES_OPTION) {
                    salasManager.eliminarSala(salaSeleccionada.nombre); 
                    cargarListaSalas(); 
                }
            });
            
            menu.add(itemEliminar);
            
            menu.show(listaSalas, evt.getX(), evt.getY());
            return;
        }

        // Conectar al doble click
        if (evt.getClickCount() == 2 && javax.swing.SwingUtilities.isLeftMouseButton(evt)) {
            btnConectarActionPerformed(null); 
        }
    }//GEN-LAST:event_listaSalasMouseClicked

