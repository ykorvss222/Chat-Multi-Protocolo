        public java.awt.Component getListCellRendererComponent(javax.swing.JList<? extends InfoSala> list, InfoSala sala, int index, boolean isSelected, boolean cellHasFocus) {
            
            // configuracion lado izq
            String estadoStr = sala.online ? "● " : "○ "; 
            lblIzquierda.setText(" " + estadoStr + " " + sala.nombre);
            lblIzquierda.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 14));
            
            // configuracion lado der
            String redStr = (sala.ip.contains("127.0.0.1") || sala.ip.contains("localhost")) ? "Local" : "Remoto";
            
            if (sala.online) {
                lblDerecha.setText(sala.usuarios + "/" + sala.max + " | " + sala.protocolo + " | " + redStr + " ");
                lblDerecha.setForeground(new java.awt.Color(0, 150, 0));
                lblIzquierda.setForeground(java.awt.Color.BLACK);
            } else {
                lblDerecha.setText("Offline | " + sala.protocolo + " | " + redStr + " ");
                lblDerecha.setForeground(java.awt.Color.GRAY);
                lblIzquierda.setForeground(java.awt.Color.GRAY);
            }

            if (isSelected) {
                setBackground(new java.awt.Color(200, 230, 255));
                setOpaque(true);
            } else if (index == indiceHover) {
                setBackground(new java.awt.Color(235, 235, 235)); 
                setOpaque(true);
            } else {
                setBackground(list.getBackground());
                setOpaque(false);
            }

            return this;
        }
