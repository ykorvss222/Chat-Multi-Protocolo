    private void checarEstadoPing(InfoSala sala) {
        try (java.net.Socket socket = new java.net.Socket()) {
            socket.connect(new java.net.InetSocketAddress(sala.ip, sala.puerto), 500);
            
            java.io.PrintWriter out = new java.io.PrintWriter(new java.io.OutputStreamWriter(socket.getOutputStream(), java.nio.charset.StandardCharsets.UTF_8), true);
            java.io.BufferedReader in = new java.io.BufferedReader(new java.io.InputStreamReader(socket.getInputStream(), java.nio.charset.StandardCharsets.UTF_8));
            
            // Mandamos el comando opara revisar el estado del servidor
            out.println("STATUS_CHECK");
            String respuesta = in.readLine();
            
            if (respuesta != null && respuesta.startsWith("STATUS:")) {
                String[] partes = respuesta.split(":");
                sala.usuarios = Integer.parseInt(partes[1]);
                sala.max = Integer.parseInt(partes[2]);
                sala.online = true;
            }
        } catch (Exception e) {
            sala.online = false; // Si hay error o no contesta a tiempo, está offline
        }
    }
    
